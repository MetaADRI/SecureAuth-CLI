function patchSqlInJs(content, dbType) {
  // Match CREATE TABLE users (...) inside template literal or string
  const createTableRegex =
    /(CREATE\s+TABLE\s+(?:IF\s+NOT\s+EXISTS\s+)?users\s*\()([\s\S]*?)(\n\s*\))/i;
  const match = content.match(createTableRegex);
  if (!match) return { content, modified: false };

  const cols = match[2];
  if (/failed_login_attempts/.test(cols)) {
    return { content, modified: false };
  }

  // Insert before the closing paren of the column list
  const colDef = generateLockoutColumnsForSchema(dbType);
  // Ensure trailing comma on last existing column line
  let body = cols.replace(/\s*$/, '');
  if (!body.trimEnd().endsWith(',')) {
    body = body.trimEnd() + ',';
  }
  const patchedTable = match[1] + body + '\n' + colDef + match[3];
  return {
    content: content.replace(match[0], patchedTable),
    modified: true
  };
}

function patchRawSql(content, dbType) {
  return patchSqlInJs(content, dbType);
}

function patchKnexSchema(content, dbType) {
  const knexRegex =
    /createTable\s*\(\s*['"]users['"]\s*,\s*(?:function\s*\(\s*table\s*\)|\(\s*table\s*\)|\(\s*t\s*\)|\(\s*b\s*\)\s*=>|table\s*=>)/i;
  const match = content.match(knexRegex);
  if (!match) return { content, modified: false };

  const afterMatch = content.slice(match.index + match[0].length);
  const closingRegex = /\}\s*\)/;
  const closingMatch = afterMatch.match(closingRegex);
  if (!closingMatch) return { content, modified: false };

  const insertPos = match.index + match[0].length + closingMatch.index;
  const colDef =
    dbType === 'postgres'
      ? `\n    table.integer('failed_login_attempts').defaultTo(0);\n    table.timestamp('locked_until');\n    table.timestamp('last_failed_login');`
      : `\n    table.integer('failed_login_attempts').defaultTo(0);\n    table.string('locked_until');\n    table.string('last_failed_login');`;

  return {
    content: content.slice(0, insertPos) + colDef + '\n  ' + content.slice(insertPos),
    modified: true
  };
}

function patchSequelizeModel(content, dbType) {
  const initRegex = /((?:\.init|\.define)\s*\([^)]*\{[\s\S]*?)(\}\s*,)/;
  const match = content.match(initRegex);
  if (!match) return { content, modified: false };

  const colDef =
    dbType === 'postgres'
      ? `\n    failed_login_attempts: { type: DataTypes.INTEGER, defaultValue: 0 },\n    locked_until: DataTypes.DATE,\n    last_failed_login: DataTypes.DATE,`
      : `\n    failed_login_attempts: { type: DataTypes.INTEGER, defaultValue: 0 },\n    locked_until: DataTypes.TEXT,\n    last_failed_login: DataTypes.TEXT,`;

  return {
    content: content.replace(match[0], match[1] + colDef + '\n  ' + match[2]),
    modified: true
  };
}

function findSqliteDbFile(targetDir) {
  const candidates = [
    path.join(targetDir, 'cavendish.db'),
    path.join(targetDir, 'database.db'),
    path.join(targetDir, 'app.db'),
    path.join(targetDir, 'data.db'),
    path.join(targetDir, 'sqlite.db'),
    path.join(targetDir, 'secureauth.db'),
    path.join(targetDir, 'backend', 'database.db'),
    path.join(targetDir, 'backend', 'cavendish.db'),
    path.join(targetDir, 'src', 'database.db')
  ];
  for (const p of candidates) {
    if (fs.existsSync(p)) return p;
  }

  // Scan common dirs for *.db
  const scanDirs = [targetDir, path.join(targetDir, 'backend'), path.join(targetDir, 'database'), path.join(targetDir, 'data')];
  for (const dir of scanDirs) {
    if (!fs.existsSync(dir)) continue;
    try {
      for (const f of fs.readdirSync(dir)) {
        if (f.endsWith('.db') && !f.includes('-shm') && !f.includes('-wal')) {
          return path.join(dir, f);
        }
      }
    } catch {}
  }
  return null;
}

function loadBetterSqlite3(targetDir) {
  // Prefer the target project's better-sqlite3 (critical when CLI is run via npx from cache)
  try {
    const resolved = require.resolve('better-sqlite3', { paths: [targetDir] });
    return require(resolved);
  } catch {}
  try {
    return require('better-sqlite3');
  } catch {}
  return null;
}

function migrateExistingDb(targetDir, dbType) {
  if (dbType !== 'sqlite') {
    logger.info('Automatic live migration is only supported for SQLite');
    logger.info('Run migration_lockout.sql manually for PostgreSQL/MySQL');
    return false;
  }

  const dbPath = findSqliteDbFile(targetDir);
  if (!dbPath) {
    logger.info('No existing SQLite database found — columns will be added when the schema runs');
    return false;
  }

  const Database = loadBetterSqlite3(targetDir);
  if (!Database) {
    logger.warn('better-sqlite3 not available — skipping live database migration');
    logger.info('Install better-sqlite3, then re-run init, or apply migration_lockout.sql');
    return false;
  }

  const columns = [
    { name: 'failed_login_attempts', sql: 'INTEGER DEFAULT 0' },
    { name: 'locked_until', sql: 'TEXT' },
    { name: 'last_failed_login', sql: 'TEXT' }
  ];

  let db;
  try {
    db = new Database(dbPath);
    // Ensure users table exists
    const table = db.prepare("SELECT name FROM sqlite_master WHERE type='table' AND name='users'").get();
    if (!table) {
      logger.warn('No users table found in database — skip live migration');
      return false;
    }

    const existing = new Set(
      db.prepare('PRAGMA table_info(users)').all().map((c) => c.name)
    );

    let added = 0;
    for (const col of columns) {
      if (existing.has(col.name)) continue;
      try {
        db.exec(`ALTER TABLE users ADD COLUMN ${col.name} ${col.sql}`);
        added++;
      } catch (e) {
        if (!/duplicate column/i.test(e.message)) throw e;
      }
    }

    if (added > 0) {
      logger.success(
        `Added ${added} lockout column(s) to ${path.relative(targetDir, dbPath)}`
      );
    } else {
      logger.info('Database already has all lockout columns');
    }
    return true;
  } catch (e) {
    logger.warn(`Could not migrate existing database: ${e.message}`);
    logger.info('You may need to run migration_lockout.sql manually');
    return false;
  } finally {
    if (db) db.close();
  }
}

function patchSchemaFile(targetDir, dbType) {
  const schemaFile = findSchemaFile(targetDir);
  if (!schemaFile) {
    logger.info('No schema file found — generating migration SQL instead');
    writeMigration(targetDir, dbType);
    migrateExistingDb(targetDir, dbType);
    return null;
  }

  // Skip pure connection modules that only open the DB (no CREATE TABLE)
  let content = fs.readFileSync(schemaFile, 'utf-8');
  if (!/CREATE\s+TABLE/i.test(content) && path.basename(schemaFile) === 'database.js') {
    // Try initDatabase next
    const initPath = path.join(targetDir, 'initDatabase.js');
    if (fs.existsSync(initPath)) {
      return patchSchemaFileWithPath(targetDir, dbType, initPath);
    }
  }

  return patchSchemaFileWithPath(targetDir, dbType, schemaFile);
}

function patchSchemaFileWithPath(targetDir, dbType, schemaFile) {
  let content = fs.readFileSync(schemaFile, 'utf-8');

  if (isSchemaAlreadyPatched(content)) {
    logger.info(`Schema already has lockout columns: ${path.relative(targetDir, schemaFile)}`);
    migrateExistingDb(targetDir, dbType);
    writeMigration(targetDir, dbType);
    return null;
  }

  // Only patch files that define a users table
  if (!/CREATE\s+TABLE/i.test(content) && !/createTable\s*\(/i.test(content) && !/DataTypes/i.test(content)) {
    logger.info(`${path.relative(targetDir, schemaFile)} has no users schema — generating migration SQL`);
    writeMigration(targetDir, dbType);
    migrateExistingDb(targetDir, dbType);
    return null;
  }

  const schemaStyle = detectSchemaStyle(content);
  logger.info(`Found schema file: ${path.relative(targetDir, schemaFile)} (style: ${schemaStyle})`);

  let result;
  switch (schemaStyle) {
    case 'sqlite-exec':
      result = patchSqlInJs(content, dbType);
      break;
    case 'sql':
      result = patchRawSql(content, dbType);
      break;
    case 'knex':
      result = patchKnexSchema(content, dbType);
      break;
    case 'sequelize':
      result = patchSequelizeModel(content, dbType);
      break;
    default:
      logger.warn(`Unknown schema style — generating migration SQL instead`);
      writeMigration(targetDir, dbType);
      migrateExistingDb(targetDir, dbType);
      return null;
  }

  if (!result.modified) {
    logger.warn('Could not auto-patch schema — generating migration SQL instead');
    writeMigration(targetDir, dbType);
    migrateExistingDb(targetDir, dbType);
    return null;
  }

  fs.writeFileSync(schemaFile, result.content, 'utf-8');
  logger.success(`Patched ${path.relative(targetDir, schemaFile)} with lockout columns`);
  writeMigration(targetDir, dbType);
  migrateExistingDb(targetDir, dbType);
  return null;
}

/**
 * Main entry: patch an existing Express app with account lockout.
 */
function patchExisting(targetDir, answers) {
  const hasLockout = answers.features && answers.features.includes('accountLockout');
  if (!hasLockout) {
    logger.info('Account lockout not selected — skipping patcher');
    return { patched: false };
  }

  const authFile = findAuthRouteFile(targetDir);
  if (!authFile) {
    logger.warn('Could not find existing auth/login route file — lockout not patched');
    logger.info('Expected paths like routes/auth.js or backend/routes/auth.js');
    return { patched: false };
  }

  logger.info(`Found auth route: ${path.relative(targetDir, authFile)}`);

  const dbType = detectDatabaseType(targetDir);
  let content = fs.readFileSync(authFile, 'utf-8');
  const paramStyle = detectParamStyle(content);
  const apiStyle = detectApiStyle(content);
  const dbVar = detectDbVariable(content);

  logger.info(
    `Database: ${dbType}, params: ${paramStyle}, API: ${apiStyle}, DB var: \`${dbVar}\``
  );

  // Write reusable lockout module
  const modulePath = writeLockoutModule(targetDir, authFile, apiStyle, paramStyle);

  // Wire module into login
  const result = patchAuthRoute(authFile, modulePath, dbVar, paramStyle);
  if (result.modified && !result.alreadyPatched) {
    logger.success(`Patched ${path.relative(targetDir, authFile)} with account lockout`);
  }

  // Schema + live DB
  patchSchemaFile(targetDir, dbType);

  logger.info('Lockout env defaults (also written to .env when possible):');
  logger.info('  MAX_FAILED_ATTEMPTS=5');
  logger.info('  LOCKOUT_DURATION_MINUTES=30');

  return {
    patched: true,
    authFile,
    modulePath,
    alreadyPatched: !!result.alreadyPatched,
    dbType
  };
}

